public class Generica 
{
    int id_registro;
    String nome_Registro;
    String descricao_Registro;

    public Generica (int id_registro, String nome_Registro, String descricao_Registro)
    {
        this.id_registro = id_registro;
        this.nome_Registro = nome_Registro;
        this.descricao_Registro = descricao_Registro;
    }
}